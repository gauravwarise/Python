x1=int(input("Enter first number:"))
x2=int(input("Enter second number:"))

def add_num(a,b):
    sum=a+b
    return sum
print("Addition is :",add_num(x1,x2))

def sub_num(a,b):
    sub=a-b
    return sub
print("substraction is :",sub_num(x1,x2))

def div_num(a,b):
    div=a/b
    return div
print("division is :",div_num(x1,x2))

def mult_num(a,b):
    mult=a+b
    return mult
print("multiplication is :",mult_num(x1,x2))